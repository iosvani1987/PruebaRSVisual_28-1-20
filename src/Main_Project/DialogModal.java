    private void btn_SelectedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SelectedActionPerformed
        // TODO add your handling code here:
        if (list_Wind.getSelectedIndices().length > 0) {
            int index = list_Wind.getSelectedIndices().length - 1;
            while (list_Wind.getSelectedIndices().length != 0) {
                model_out.addElement(model_in.getElementAt(list_Wind.getSelectedIndices()[index]));
                model_in.remove(list_Wind.getSelectedIndices()[index--]);
            }
            list_Wind.setModel(model_in);
            list_Wind_selected.setModel(model_out);
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione alguna Ventana!!!", "INFORMACIÓN", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_SelectedActionPerformed

