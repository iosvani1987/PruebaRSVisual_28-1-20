     /**
     * RECORRER EL ARBOL EN POSTORDEN*
     */
    public void postOrden_Fill_List(Arbolphsh<WindowsFringes> tree) {
        postOrdenR_Fill_List(tree.getRaiz());
    }

    private void postOrdenR_Fill_List(Nodo<WindowsFringes> nodo) {
        if (nodo != null) {
            Nodo<WindowsFringes> ph = nodo.getPH();
            postOrdenR_Fill_List(ph);
            while (ph != null) {
                ph = ph.getSH();
                postOrdenR_Fill_List(ph);
            }
            // PREGUNTO SI EL NODO ES HOJA Y SI SI LLAMO AL ALGORITMO DE RS
            if (nodo.getPH() == null) {
                // Anhado un elemento al List
                String nameWind = nodo.getInfo().getName();
                model_in.addElement(nameWind);
                list_Wind.setModel(model_in);
            }

        }
    }

